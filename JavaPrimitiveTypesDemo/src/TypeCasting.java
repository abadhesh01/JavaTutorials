
public class TypeCasting {
 public static void main(String[] args) {
   
	 /*
	  * Type Casting:
	  * -------------                            Explicit Casting/ Downcasting (Manual casting)   
	  *         |                               /\       
	  *  byte   |                               |
	  *  short  |                               |
	  *  int    |                               |
	  *  long   |                               |
	  *  float  |                               |
	  *  double |                               |
	  *         \/                              |
	  *         Implicit Casting/
	  *         Upcasting
	  *         (Automatic casting)
	  */
	 
	 // Implicit casting 
	 byte sampleShortValue = 122;
	 int sampleIntValue = sampleShortValue;
	 long sampleLongValue = sampleIntValue;
	 double sampleDoubleValue = sampleLongValue;
	 
	 // Explicit casting
	 double sampleDoubleValue2 = 128.775;
	 float sampleFloatValue = (float) sampleDoubleValue2;
	 int sampleIntValue2 = (int) sampleFloatValue;
	 byte sampleByteValue2 = (byte) sampleIntValue2;
	 
	 
	 System.out.println("Execution successful.");
 }	
}
